tvzStore.config(function ($stateProvider,$urlRouterProvider) {
    $stateProvider.state('home',{
        url:'/',
        component:'home'
    }).state('login',{
        url:'/login',
        component:'login'
    }).state('register',{
        url:'/register',
        component:'register'
    }).state('admin',{
        url:'/admin',
        component:'administration'
    }).state('userAdmin',{
        url:'/admin/user',
        component:'userAdministration'
    }).state('itemAdmin',{
        url:'/admin/item',
        component:'itemAdministration'
    }).state('categoryAdmin',{
        url:'/admin/category',
        component:'categoryAdministration'
    }).state('orderAdmin',{
        url:'/admin/order',
        component:'orderAdministration'
    }).state('itemsByCategory',{
        url:'/items/:categoryName',
        component:'itemByCategory'
    });
    $urlRouterProvider.otherwise('/');
})